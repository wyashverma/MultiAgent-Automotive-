class AutomotiveResearcherModule:
    """Complete researcher module for integration with Supervisor"""

    def __init__(self):
        self.researcher = ResearcherAgent()
        self.shared_memory = {
            "research_data": {},
            "pending_requests": [],
            "completed_research": [],
            "module_status": "ready"
        }
        self.request_counter = 0

    def process_request(self, request: Dict) -> Dict:
        """
        Process a research request from the supervisor

        Expected request format:
        {
            "task": "research_cars",
            "cars": ["Tesla Model 3", "BMW i4"],
            "priority": "high",
            "request_id": "optional-id"
        }
        """
        # Generate request ID if not provided
        self.request_counter += 1
        request_id = request.get("request_id", f"REQ-{self.request_counter:03d}")
        cars_to_research = request.get("cars", [])

        print(f"\n Processing request {request_id}")
        print(f"   Cars to research: {cars_to_research}")

        # Add to pending
        self.shared_memory["pending_requests"].append(request_id)

        try:
            # Research the cars
            results = self.researcher.research_multiple_cars(cars_to_research)

            # Format for shared memory
            formatted_results = self.researcher.format_for_shared_memory(results)

            # Update shared memory
            self.shared_memory["research_data"].update(formatted_results["research_data"])
            self.shared_memory["completed_research"].append({
                "request_id": request_id,
                "cars": cars_to_research,
                "timestamp": datetime.now().isoformat(),
                "status": "success"
            })

            # Remove from pending
            if request_id in self.shared_memory["pending_requests"]:
                self.shared_memory["pending_requests"].remove(request_id)

            # Return success response
            return {
                "status": "success",
                "request_id": request_id,
                "data": formatted_results,
                "message": f"Successfully researched {len(cars_to_research)} cars",
                "cars_completed": cars_to_research
            }

        except Exception as e:
            error_msg = f"Error: {str(e)}"
            print(f" {error_msg}")

            self.shared_memory["completed_research"].append({
                "request_id": request_id,
                "cars": cars_to_research,
                "timestamp": datetime.now().isoformat(),
                "status": "failed",
                "error": error_msg
            })

            return {
                "status": "error",
                "request_id": request_id,
                "error": error_msg,
                "data": None
            }

    def get_status(self, request_id: str = None) -> Dict:
        """Get status of research requests"""
        if request_id:
            if request_id in self.shared_memory["pending_requests"]:
                return {"status": "pending", "request_id": request_id}
            elif any(r["request_id"] == request_id for r in self.shared_memory["completed_research"]):
                return {"status": "completed", "request_id": request_id}
            else:
                return {"status": "not_found", "request_id": request_id}
        else:
            return {
                "module_status": self.shared_memory["module_status"],
                "pending_requests": self.shared_memory["pending_requests"],
                "completed_count": len(self.shared_memory["completed_research"]),
                "cars_in_memory": list(self.shared_memory["research_data"].keys()),
                "total_requests_processed": self.request_counter
            }

