class ChartGenerator:
    """Generate charts for visual comparisons"""

    @staticmethod
    def create_comparison_chart(car_data: Dict[str, Any], filename: str = "comparison_chart.png"):
        """Create a simple bar chart for comparison"""
        try:
            cars = list(car_data.keys())
            metrics = ['Performance', 'Efficiency', 'Value']

            # Calculate scores for each car
            scores = {}
            for car_name, data in car_data.items():
                specs = data.get('specifications', {})
                scores[car_name] = [
                    ChartGenerator._calculate_score(specs, 'performance'),
                    ChartGenerator._calculate_score(specs, 'efficiency'),
                    ChartGenerator._calculate_score(specs, 'value')
                ]

            # Create bar chart
            x = np.arange(len(metrics))
            width = 0.25
            multiplier = 0

            fig, ax = plt.subplots(figsize=(10, 6))

            for car_name, car_scores in scores.items():
                offset = width * multiplier
                rects = ax.bar(x + offset, car_scores, width, label=car_name)
                multiplier += 1

            ax.set_ylabel('Score (0-10)')
            ax.set_title('Car Comparison by Category')
            ax.set_xticks(x + width)
            ax.set_xticklabels(metrics)
            ax.legend(loc='upper right')
            ax.set_ylim(0, 10)

            plt.tight_layout()
            plt.savefig(filename, dpi=100, bbox_inches='tight')
            plt.close()

            return filename

        except Exception as e:
            print(f"Error creating chart: {e}")
            return None

    @staticmethod
    def _calculate_score(specs: Dict, metric: str) -> float:
        """Calculate score for a metric"""
        score = 5.0  # Base score

        if metric == 'performance':
            if specs.get('horsepower'):
                hp = float(specs['horsepower'])
                if hp > 400:
                    score += 3
                elif hp > 300:
                    score += 2
                elif hp > 200:
                    score += 1

            if specs.get('acceleration'):
                acc = float(specs['acceleration'])
                if acc < 4:
                    score += 2
                elif acc < 5:
                    score += 1

        elif metric == 'efficiency':
            if specs.get('range_miles'):
                range_val = float(specs['range_miles'])
                if range_val > 350:
                    score += 3
                elif range_val > 300:
                    score += 2
                elif range_val > 250:
                    score += 1

        elif metric == 'value':
            if specs.get('price'):
                price = float(specs['price'])
                if price < 40000:
                    score += 3
                elif price < 50000:
                    score += 2
                elif price < 60000:
                    score += 1

        return min(10.0, max(0.0, round(score, 1)))

