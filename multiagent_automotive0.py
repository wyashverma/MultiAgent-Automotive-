    def _calculate_performance_score(self, specs: Dict) -> float:
        """Calculate performance score (0-10)"""
        score = 5.0

        if specs.get('horsepower'):
            hp = float(specs['horsepower'])
            if hp > 400:
                score += 3
            elif hp > 300:
                score += 2
            elif hp > 200:
                score += 1

        if specs.get('acceleration'):
            acc = float(specs['acceleration'])
            if acc < 4:
                score += 3
            elif acc < 5:
                score += 2
            elif acc < 6:
                score += 1

        return min(10.0, max(0.0, round(score, 1)))

    def _calculate_efficiency_score(self, specs: Dict) -> float:
        """Calculate efficiency score (0-10)"""
        score = 5.0

        if specs.get('range_miles'):
            range_val = float(specs['range_miles'])
            if range_val > 350:
                score += 4
            elif range_val > 300:
                score += 3
            elif range_val > 250:
                score += 2
            elif range_val > 200:
                score += 1

        return min(10.0, max(0.0, round(score, 1)))

    def _calculate_value_score(self, specs: Dict) -> float:
        """Calculate value score (0-10)"""
        score = 5.0

        if specs.get('price') and specs.get('range_miles'):
            price = float(specs['price'])
            range_val = float(specs['range_miles'])

            # Value = range per $1000
            value_ratio = range_val / (price / 1000)

            if value_ratio > 8:
                score += 4
            elif value_ratio > 7:
                score += 3
            elif value_ratio > 6:
                score += 2
            elif value_ratio > 5:
                score += 1

        return min(10.0, max(0.0, round(score, 1)))

    def _calculate_tech_score(self, specs: Dict) -> float:
        """Calculate technology score (0-10)"""
        score = 5.0

        if specs.get('battery_capacity'):
            battery = float(specs['battery_capacity'])
            if battery > 90:
                score += 3
            elif battery > 75:
                score += 2
            elif battery > 60:
                score += 1

        if specs.get('drivetrain'):
            if 'AWD' in str(specs['drivetrain']):
                score += 2
            elif 'Dual Motor' in str(specs['drivetrain']):
                score += 2

        return min(10.0, max(0.0, round(score, 1)))

    def _generate_recommendation(self, car1: str, car2: str,
                                 car1_data: Dict, car2_data: Dict) -> str:
        """Generate recommendation based on comparison"""

        specs1 = car1_data.get('specifications', {})
        specs2 = car2_data.get('specifications', {})

        # Calculate overall scores
        score1 = (
            self._calculate_performance_score(specs1) +
            self._calculate_efficiency_score(specs1) +
            self._calculate_value_score(specs1) +
            self._calculate_tech_score(specs1)
        ) / 4

        score2 = (
            self._calculate_performance_score(specs2) +
            self._calculate_efficiency_score(specs2) +
            self._calculate_value_score(specs2) +
            self._calculate_tech_score(specs2)
        ) / 4

        if score1 > score2:
            winner = car1
            diff = round(score1 - score2, 1)
            return f"Based on our comprehensive analysis, the **{car1}** edges out the {car2} with a score of {score1}/10 vs {score2}/10 (difference of {diff} points). It offers better overall value and performance for most buyers."
        elif score2 > score1:
            winner = car2
            diff = round(score2 - score1, 1)
            return f"Based on our comprehensive analysis, the **{car2}** outperforms the {car1} with a score of {score2}/10 vs {score1}/10 (difference of {diff} points). It provides superior features and driving experience."
        else:
            return f"Both vehicles are evenly matched with scores of {score1}/10. Your choice should depend on specific priorities like brand preference, local dealer support, or specific feature requirements."

    def _determine_winners(self, cars_data: Dict) -> List[Dict]:
        """Determine winners by category"""
        winners_list = []

        categories = {
            "Performance": "horsepower",
            "Range": "range_miles",
            "Value": "price",
            "Acceleration": "acceleration"
        }

        for category, spec_key in categories.items():
            best_car = None
            best_value = None

            for car_name, car_data in cars_data.items():
                specs = car_data.get('specifications', {})
                value = specs.get(spec_key)

                if value is not None:
                    if spec_key == 'price':  # Lower price is better for value
                        if best_value is None or value < best_value:
                            best_value = value
                            best_car = car_name
                    else:  # Higher is better for other metrics
                        if best_value is None or value > best_value:
                            best_value = value
                            best_car = car_name

            if best_car:
                if spec_key == 'price':
                    reason = f"Best value with ${best_value:,}"
                elif spec_key == 'horsepower':
                    reason = f"Most powerful with {best_value} hp"
                elif spec_key == 'range_miles':
                    reason = f"Longest range at {best_value} miles"
                elif spec_key == 'acceleration':
                    reason = f"Quickest 0-60 mph at {best_value}s"
                else:
                    reason = f"Best in category with {best_value}"

                winners_list.append({
                    "name": category,
                    "winner": best_car,
                    "reason": reason
                })

        return winners_list

    def _generate_multi_car_recommendation(self, cars_data: Dict, scores_list: List) -> str:
        """Generate recommendation for multiple cars"""

        # Find highest overall score
        if scores_list:
            best_car = max(scores_list, key=lambda x: x['overall'])

            # Get strengths
            strengths = []
            if best_car['performance'] >= 8:
                strengths.append("performance")
            if best_car['efficiency'] >= 8:
                strengths.append("efficiency")
            if best_car['value'] >= 8:
                strengths.append("value")
            if best_car['tech'] >= 8:
                strengths.append("technology")

            if strengths:
                strength_text = f"excelling particularly in {', '.join(strengths)}"
            else:
                strength_text = "offering balanced performance across all categories"

            return f"After detailed analysis, the **{best_car['name']}** emerges as the top choice with an overall score of {best_car['overall']}/10, {strength_text}. However, we recommend test driving all contenders as personal preference plays a crucial role in the final decision."
        else:
            return "Unable to generate recommendation due to insufficient data."

